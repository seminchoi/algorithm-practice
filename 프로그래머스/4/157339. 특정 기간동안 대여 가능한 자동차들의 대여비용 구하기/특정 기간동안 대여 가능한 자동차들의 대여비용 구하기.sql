WITH FEE_AMOUNT AS (
    SELECT C.CAR_ID, C.CAR_TYPE, FLOOR(C.DAILY_FEE * 30 * (100 - D.DISCOUNT_RATE) / 100) as FEE
    FROM CAR_RENTAL_COMPANY_CAR AS C
    JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN AS D ON C.CAR_TYPE = D.CAR_TYPE AND D.DURATION_TYPE = "30일 이상"
)
SELECT C.CAR_ID, C.CAR_TYPE, C.FEE
FROM FEE_AMOUNT AS C
WHERE C.CAR_ID NOT IN (
        SELECT CAR_ID
        FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY as H
        WHERE (H.START_DATE <= '2022-11-01' AND H.END_DATE >= '2022-11-30')
        OR (H.START_DATE >= '2022-11-01' AND H.START_DATE <= '2022-11-30')
        OR (H.END_DATE > '2022-11-01' AND H.END_DATE <= '2022-11-30')
    )
    AND C.FEE >= 500000 AND C.FEE <= 2000000
ORDER BY C.FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC;


 
